// Package tsrender generates TypeScript source code from the Go-oriented
// intermediate representation produced by the transform package. It outputs
// .ts files containing interfaces, enums, enum name mappings, and (later)
// validation metadata.
package tsrender

import (
	"fmt"
	"strings"

	"github.com/pinealctx/gcode/internal/model"
	"github.com/pinealctx/gcode/internal/naming"
	"github.com/pinealctx/gcode/internal/transform"
)

// TSFile generates a complete TypeScript file from a GoFile intermediate
// representation. The returned bytes are ready to write to disk as a .pb.ts file.
func TSFile(gf transform.GoFile) ([]byte, error) {
	var b strings.Builder

	writeHeader(&b, gf.Source)

	for _, enum := range gf.Enums {
		writeTSEnum(&b, enum)
	}

	for _, msg := range gf.Messages {
		writeTSInterface(&b, msg, gf.Package)
	}

	return []byte(b.String()), nil
}

// tsScalarType maps a protobuf scalar kind to its TypeScript type.
func tsScalarType(kind model.ScalarKind) string {
	switch kind {
	case model.ScalarInt32, model.ScalarSint32, model.ScalarSfixed32,
		model.ScalarUint32, model.ScalarFixed32,
		model.ScalarFloat, model.ScalarDouble:
		return "number"
	case model.ScalarInt64, model.ScalarSint64, model.ScalarSfixed64,
		model.ScalarUint64, model.ScalarFixed64:
		return "string"
	case model.ScalarBool:
		return "boolean"
	case model.ScalarString, model.ScalarBytes:
		return "string"
	default:
		return "unknown"
	}
}

// tsFieldType resolves the TypeScript type string for a GoField.
// It handles scalars, enums, message references, and repeated cardinality.
func tsFieldType(f transform.GoField, pkgName string) string {
	var base string
	switch f.Type.Kind {
	case model.FieldKindScalar:
		base = tsScalarType(f.Type.Scalar)
	case model.FieldKindEnum, model.FieldKindMessage:
		base = naming.GoTypeName(f.Type.FullName, pkgName)
	default:
		base = "unknown"
	}

	if f.Cardinality == model.CardinalityRepeated {
		return base + "[]"
	}
	return base
}

// tsIsOptional returns true if the field should be marked optional in the
// TypeScript interface (using the `?:` syntax). This covers both proto3
// explicit optional fields and fields with presence semantics (e.g. optional bytes).
func tsIsOptional(f transform.GoField) bool {
	return f.Optional || f.HasPresence
}

// writeHeader writes the generated-file header comment.
func writeHeader(b *strings.Builder, source string) {
	b.WriteString("// Code generated by gcode. DO NOT EDIT.\n")
	fmt.Fprintf(b, "// source: %s\n\n", source)
}

// writeTSComment writes a JSDoc-style comment block. Single-line comments
// use the `/** ... */` form; multi-line comments use the indented form.
func writeTSComment(b *strings.Builder, comment model.Comment) {
	if len(comment.Lines) == 0 {
		return
	}
	if len(comment.Lines) == 1 {
		fmt.Fprintf(b, "/** %s */\n", comment.Lines[0])
		return
	}
	b.WriteString("/**\n")
	for _, line := range comment.Lines {
		fmt.Fprintf(b, " * %s\n", line)
	}
	b.WriteString(" */\n")
}

// writeTSEnum writes a TypeScript enum declaration followed by its
// name mapping Record. Enum value names are extracted from the GoName
// by stripping the parent enum name prefix.
//
// Example output:
//
//	export enum Status {
//	  STATUS_UNSPECIFIED = 0,
//	  STATUS_ACTIVE = 1,
//	}
//
//	export const StatusName: Record<Status, string> = {
//	  [Status.STATUS_UNSPECIFIED]: "STATUS_UNSPECIFIED",
//	  [Status.STATUS_ACTIVE]: "STATUS_ACTIVE",
//	} as const
func writeTSEnum(b *strings.Builder, enum transform.GoEnum) {
	writeTSComment(b, enum.Comment)
	fmt.Fprintf(b, "export enum %s {\n", enum.GoName)
	for _, v := range enum.Values {
		protoName := strings.TrimPrefix(v.GoName, enum.GoName+"_")
		fmt.Fprintf(b, "  %s = %d,\n", protoName, v.Number)
	}
	b.WriteString("}\n\n")

	fmt.Fprintf(b, "export const %sName: Record<%s, string> = {\n", enum.GoName, enum.GoName)
	for _, v := range enum.Values {
		protoName := strings.TrimPrefix(v.GoName, enum.GoName+"_")
		fmt.Fprintf(b, "  [%s.%s]: \"%s\",\n", enum.GoName, protoName, protoName)
	}
	b.WriteString("} as const\n\n")
}

// writeTSInterface writes a TypeScript interface declaration for a message.
// Field names use the proto JSONName (camelCase). Optional fields use the
// `?:` syntax; repeated fields use `T[]`.
//
// Example output:
//
//	/** Comment from proto. */
//	export interface Person {
//	  name: string
//	  age: number
//	  address?: Address
//	  scores: number[]
//	}
func writeTSInterface(b *strings.Builder, msg transform.GoMessage, pkgName string) {
	writeTSComment(b, msg.Comment)
	fmt.Fprintf(b, "export interface %s {\n", msg.GoName)
	for _, f := range msg.Fields {
		fieldName := f.JSONName
		fieldType := tsFieldType(f, pkgName)
		if tsIsOptional(f) {
			fmt.Fprintf(b, "  %s?: %s\n", fieldName, fieldType)
		} else {
			fmt.Fprintf(b, "  %s: %s\n", fieldName, fieldType)
		}
	}
	b.WriteString("}\n\n")
}
