// TestToMapUsesGormColumn verifies that ToMap() uses the gorm column name as
// the map key when a field has a gorm.column override.
func TestToMapUsesGormColumn(t *testing.T) {
	t.Parallel()

	gf := transform.GoFile{
		Source:  "item.update.proto",
		Package: "foopb",
		Messages: []transform.GoMessage{
			{
				GoName:       "ItemUpdate",
				UpdateSource: "Item",
				Fields: []transform.GoField{
					{
						Field: model.Field{
							Name: "created_at", Number: 1,
							Cardinality: model.CardinalitySingular,
							Type:        model.FieldType{Kind: model.FieldKindScalar, Scalar: model.ScalarInt64},
							JSONName:    "createdAt",
							GormOptions: &model.GormFieldOptions{Column: "created_ts"},
						},
						GoName: "CreatedAt",
						GoType: "*int64",
					},
					{
						Field: model.Field{
							Name: "name", Number: 2,
							Cardinality: model.CardinalitySingular,
							Type:        model.FieldType{Kind: model.FieldKindScalar, Scalar: model.ScalarString},
							JSONName:    "name",
						},
						GoName: "Name",
						GoType: "string",
					},
				},
			},
		},
	}

	got, err := File(gf, testModule, Context{})
	if err != nil {
		t.Fatalf("File() error: %v", err)
	}
	src := string(got)

	// gorm column override: key must be "created_ts", not "created_at" or "createdAt"
	if !strings.Contains(src, `um["created_ts"]`) {
		t.Errorf("ToMap() must use gorm column name 'created_ts' as key, got:\n%s", src)
	}
	if strings.Contains(src, `um["created_at"]`) || strings.Contains(src, `um["createdAt"]`) {
		t.Errorf("ToMap() must not use proto field name or json name as key, got:\n%s", src)
	}
	// no override: key must be proto field name "name"
	if !strings.Contains(src, `um["name"]`) {
		t.Errorf("ToMap() must use proto field name 'name' when no gorm column override, got:\n%s", src)
	}
}

// TestFileCreateMessageInheritsGormOptionsFromContext verifies that File()
// propagates GormMessageOptions from the source message (via Context.MessageIndex)
// to a create derived message, generating TableName() and gorm struct tags.
func TestFileCreateMessageInheritsGormOptionsFromContext(t *testing.T) {
	t.Parallel()

	gormOpts := &model.GormMessageOptions{Table: "persons"}
	srcMsg := transform.GoMessage{
		GoName:             "Person",
		GormMessageOptions: gormOpts,
		Fields: []transform.GoField{
			{
				Field:              model.Field{Name: "name", Number: 1, Cardinality: model.CardinalitySingular, Type: model.FieldType{Kind: model.FieldKindScalar, Scalar: model.ScalarString}, JSONName: "name"},
				GoName:             "Name",
				GoType:             "string",
				GormMessageOptions: gormOpts,
			},
		},
	}

	createMsg := transform.GoMessage{
		GoName:       "PersonCreate",
		CreateSource: "Person",
		Fields: []transform.GoField{
			{
				Field:  model.Field{Name: "name", Number: 1, Cardinality: model.CardinalitySingular, Type: model.FieldType{Kind: model.FieldKindScalar, Scalar: model.ScalarString}, JSONName: "name"},
				GoName: "Name",
				GoType: "*string",
			},
		},
	}

	gf := transform.GoFile{
		Source:   "person.create.proto",
		Package:  "foopb",
		Messages: []transform.GoMessage{createMsg},
	}
	ctx := Context{MessageIndex: map[string]*transform.GoMessage{"Person": &srcMsg}}

	got, err := File(gf, testModule, ctx)
	if err != nil {
		t.Fatalf("File() error: %v", err)
	}
	src := string(got)

	if !strings.Contains(src, `func (PersonCreate) TableName() string { return "persons" }`) {
		t.Errorf("PersonCreate must inherit TableName() from source message, got:\n%s", src)
	}
	if !strings.Contains(src, `gorm:"column:name"`) {
		t.Errorf("PersonCreate fields must inherit gorm tags from source message, got:\n%s", src)
	}
}

