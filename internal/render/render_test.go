// --- Deviation tests (template layer) ---

// TestDeviationPackedOnlyEnforced verifies that the generated unmarshal template
// for a packed repeated scalar field checks for WireBytes and rejects other wire types.
func TestDeviationPackedOnlyEnforced(t *testing.T) {
	t.Parallel()

	gf := transform.GoFile{
		Source:  "packed.proto",
		Package: "foopb",
		Messages: []transform.GoMessage{
			{
				GoName: "Msg",
				Fields: []transform.GoField{
					{
						Field:  model.Field{Name: "ids", Number: 1, Cardinality: model.CardinalityRepeated, Type: model.FieldType{Kind: model.FieldKindScalar, Scalar: model.ScalarInt32}, JSONName: "ids"},
						GoName: "Ids", GoType: "[]int32", ElemGoType: "int32",
					},
				},
			},
		},
	}

	got, err := File(gf, testModule, Context{})
	if err != nil {
		t.Fatalf("File() error: %v", err)
	}
	src := string(got)

	// The generated unmarshal code must require WireBytes for packed repeated int32.
	// Any other wire type must return ErrWireType.
	containsAll(t, src, map[string]string{
		"packed wire check": "runtime.WireBytes",
		"wire type error":   "runtime.ErrWireType",
	})

	// Must NOT accept WireVarint for this field (no unpacked path).
	if strings.Contains(src, "WireVarint") {
		// WireVarint may appear in other contexts (e.g. tag decode), but the
		// field-specific check must be WireBytes only. We verify by checking
		// that the packed decode path uses ConsumeBytes, not ConsumeVarint directly.
		if !strings.Contains(src, "ConsumeBytes") {
			t.Error("packed repeated field must use ConsumeBytes for the packed payload")
		}
	}
}

