const testModule = "github.com/pinealctx/gcode"

// containsAll checks that src contains all the given substrings.
func containsAll(t *testing.T, src string, checks map[string]string) {
	t.Helper()
	for label, substr := range checks {
		if !strings.Contains(src, substr) {
			t.Errorf("missing %s: %q not found in output:\n%s", label, substr, src)
		}
	}
}

func TestFileBasic(t *testing.T) {
	t.Parallel()

	gf := transform.GoFile{
		Source:  "person.proto",
		Package: "foopb",
		Messages: []transform.GoMessage{
			{
				GoName: "Person",
				Fields: []transform.GoField{
					{
						Field:  model.Field{Name: "name", Number: 1, Cardinality: model.CardinalitySingular, Type: model.FieldType{Kind: model.FieldKindScalar, Scalar: model.ScalarString}, JSONName: "name"},
						GoName: "Name", GoType: "string",
					},
					{
						Field:  model.Field{Name: "age", Number: 2, Cardinality: model.CardinalitySingular, Type: model.FieldType{Kind: model.FieldKindScalar, Scalar: model.ScalarInt32}, JSONName: "age"},
						GoName: "Age", GoType: "int32",
					},
				},
			},
		},
	}

	got, err := File(gf, testModule, Context{})
	if err != nil {
		t.Fatalf("File() error: %v", err)
	}

	src := string(got)

	containsAll(t, src, map[string]string{
		"header":  "// Code generated by gcode. DO NOT EDIT.",
		"source":  "// source: person.proto",
		"package": "package foopb",
		"struct":  "type Person struct",
		"name":    `json:"name"`,
		"age":     `json:"age"`,
	})

	// Verify field names and types are present.
	if !strings.Contains(src, "Name") || !strings.Contains(src, "string") {
		t.Error("missing Name string field")
	}
	if !strings.Contains(src, "Age") || !strings.Contains(src, "int32") {
		t.Error("missing Age int32 field")
	}

	// Verify marshal methods are generated.
	containsAll(t, src, map[string]string{
		"Size method":          "func (x *Person) Size() int",
		"MarshalBinary method": "func (x *Person) MarshalBinary() ([]byte, error)",
		"MarshalAppend method": "func (x *Person) MarshalAppend(b []byte) ([]byte, error)",
		"runtime import":       `"github.com/pinealctx/gcode/runtime"`,
	})
}

