// Package render generates Go source code from the Go-oriented intermediate
// representation produced by the transform package. It outputs formatted Go
// source bytes suitable for writing to .pb.dao.go files.
package render

import (
	"fmt"
	"go/format"
	"strings"

	"github.com/pinealctx/gcode/internal/transform"
)

// Context carries cross-file context needed during rendering.
// A zero-value Context is valid and disables context-dependent features
// (ToMap generation, validate inheritance).
type Context struct {
	// MessageIndex maps GoName to GoMessage for cross-file lookups.
	// Used by ToMap and validate inheritance to find the source message.
	MessageIndex map[string]*transform.GoMessage
	// EnumIndex maps GoName to GoEnum for cross-file enum lookups.
	// Used by validate inheritance to resolve enum defined_only checks when
	// the enum is defined in a different file from the derived message.
	EnumIndex map[string]transform.GoEnum
}

// _defaultProviders is the ordered list of TagProviders used during rendering.
// json tag is always generated as a built-in; providers add extra tags.
var _defaultProviders = []TagProvider{
	&GormTagProvider{},
}

// File renders a complete Go source file from a GoFile.
// The returned bytes are gofmt-formatted and ready to write to disk.
func File(gf transform.GoFile, modulePath string, ctx Context) ([]byte, error) {
	// Propagate GormMessageOptions from source messages to create derived messages.
	// Create derived messages have no gorm annotation of their own; they inherit
	// the source message's table name and field tag settings so that TableName()
	// and gorm struct tags are generated correctly.
	// We work on a shallow copy of the Messages slice to avoid mutating the caller's GoFile.
	if ctx.MessageIndex != nil {
		msgs := make([]transform.GoMessage, len(gf.Messages))
		copy(msgs, gf.Messages)
		for i := range msgs {
			if msgs[i].CreateSource == "" || msgs[i].GormMessageOptions != nil {
				continue
			}
			src, ok := ctx.MessageIndex[msgs[i].CreateSource]
			if !ok || src.GormMessageOptions == nil {
				continue
			}
			msgs[i].GormMessageOptions = src.GormMessageOptions
			fields := make([]transform.GoField, len(msgs[i].Fields))
			copy(fields, msgs[i].Fields)
			for j := range fields {
				fields[j].GormMessageOptions = src.GormMessageOptions
			}
			msgs[i].Fields = fields
		}
		gf.Messages = msgs
	}

	var body strings.Builder

	for _, msg := range gf.Messages {
		writeMessage(&body, msg, _defaultProviders)
	}
	for _, msg := range gf.Messages {
		writeMarshalMethods(&body, msg)
	}
	for _, msg := range gf.Messages {
		if err := writeUnmarshalMethods(&body, msg); err != nil {
			return nil, err
		}
	}
	for _, msg := range gf.Messages {
		if msg.UpdateSource != "" {
			writeToMapMethod(&body, msg, ctx)
		}
	}
	for _, enum := range gf.Enums {
		writeEnum(&body, enum)
	}

	bodyStr := body.String()

	var b strings.Builder
	writeHeader(&b, gf.Source)
	writePackage(&b, gf.Package)

	if len(gf.Messages) > 0 {
		needsMath := strings.Contains(bodyStr, "math.")
		needsFmt := strings.Contains(bodyStr, "fmt.")
		switch {
		case needsMath && needsFmt:
			fmt.Fprintf(&b, "import (\n\"fmt\"\n\"math\"\n\"%s/runtime\"\n)\n\n", modulePath)
		case needsMath:
			fmt.Fprintf(&b, "import (\n\"math\"\n\"%s/runtime\"\n)\n\n", modulePath)
		case needsFmt:
			fmt.Fprintf(&b, "import (\n\"fmt\"\n\"%s/runtime\"\n)\n\n", modulePath)
		default:
			fmt.Fprintf(&b, "import (\n\"%s/runtime\"\n)\n\n", modulePath)
		}
	}

	b.WriteString(bodyStr)

	src, err := format.Source([]byte(b.String()))
	if err != nil {
		return nil, fmt.Errorf("format generated source for %q: %w", gf.Source, err)
	}
	return src, nil
}

func writeHeader(b *strings.Builder, source string) {
	b.WriteString("// Code generated by gcode. DO NOT EDIT.\n")
	fmt.Fprintf(b, "// source: %s\n\n", source)
}

func writePackage(b *strings.Builder, pkg string) {
	fmt.Fprintf(b, "package %s\n\n", pkg)
}
