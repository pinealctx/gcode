func TestRunGenTS_BasicRouting(t *testing.T) {
	t.Parallel()

	inputDir := t.TempDir()
	outputDir := t.TempDir()

	writeFile(t, filepath.Join(inputDir, "user.proto"), `syntax = "proto3";
package test;
option go_package = "test";

message User {
  string name = 1;
  int32  age  = 2;
}
`)

	err := RunGenTS(t.Context(), []string{"-in", inputDir, "-out", outputDir})
	if err != nil {
		t.Fatalf("RunGenTS returned error: %v", err)
	}

	// Verify the generated .pb.ts file exists and contains expected output.
	tsContent, err := os.ReadFile(filepath.Join(outputDir, "user.pb.ts"))
	if err != nil {
		t.Fatalf("failed to read generated file: %v", err)
	}
	s := string(tsContent)
	if !strings.Contains(s, "// Code generated by gcode. DO NOT EDIT.") {
		t.Error("generated file missing header comment")
	}
	if !strings.Contains(s, "export interface User {") {
		t.Error("generated file missing User interface")
	}
	if !strings.Contains(s, "name: string") {
		t.Error("generated file missing name field")
	}
	if !strings.Contains(s, "age: number") {
		t.Error("generated file missing age field")
	}
}

